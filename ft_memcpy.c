#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	char	*d;
	char	*s;

	d = (char *)dst;
	s = (char *)src;
	if (!dst && !src)
		return (NULL);
	while (n > 0)
	{
		*(char *) d = *(char *) s;
		d++;
		s++;
		n--;
	}
	return (dst);
}
